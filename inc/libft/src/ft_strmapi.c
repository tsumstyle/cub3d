#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;
	int		s_len;

	i = 0;
	s_len = ft_strlen(s);
	str = malloc((s_len + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	while (i < s_len)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
